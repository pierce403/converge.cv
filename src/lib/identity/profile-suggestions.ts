export interface SuggestedDisplayName {
  displayName: string;
  color: (typeof colors)[number];
  animal: (typeof animals)[number];
}

export function isAutoGeneratedDisplayName(value?: string | null): boolean {
  const v = value?.trim();
  if (!v) return true;

  return (
    v.startsWith('Identity ') ||
    v.startsWith('Wallet ') ||
    v.startsWith('App key ') ||
    v.toLowerCase().startsWith('0x')
  );
}

export function suggestAnimalDisplayName(seed?: string | null): SuggestedDisplayName {
  const normalizedSeed = (seed || 'converge').toLowerCase();
  let hash = 2166136261 >>> 0;

  for (let i = 0; i < normalizedSeed.length; i++) {
    hash ^= normalizedSeed.charCodeAt(i);
    hash += (hash << 1) + (hash << 4) + (hash << 7) + (hash << 8) + (hash << 24);
  }

  const color = colors[Math.abs(hash) % colors.length]!;
  const animal = animals[Math.abs(hash >>> 1) % animals.length]!;

  return {
    displayName: `${color} ${animal}`,
    color,
    animal,
  };
}
